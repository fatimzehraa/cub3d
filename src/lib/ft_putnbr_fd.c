#include "libft.h"

static void	ft_putchar(char c, int fd)
{
	write (fd, &c, 1);
}

void	ft_putnbr_fd(int n, int fd)
{
	if (n == -2147483648)
		write (fd, "-2147483648", 11);
	else if (n < 0)
	{
		n = n * (-1);
		write (fd, "-", 1);
		ft_putnbr_fd(n, fd);
	}
	else if (n >= 0)
	{
		if (n >= 0 && n < 10)
		{
			ft_putchar (n + '0', fd);
		}
		if (n >= 10)
		{
			ft_putnbr_fd(n / 10, fd);
			ft_putnbr_fd(n % 10, fd);
		}
	}
}
